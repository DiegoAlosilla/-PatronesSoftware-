package model.dto;

public class Proveedor {
	private int id;
	private String razonSocial;
	private String rubro;
	private double ruc;
	private String nombreComercial;
	private String pais;
	private String ciudad;
	private String referencia;
	private String provincia;
	private String direccion;
	private String email;
	private String contacto;
	private double fax;
	private double telefono1;
	private double telefono2;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getRazonSocial() {
		return razonSocial;
	}
	public void setRazonSocial(String razonSocial) {
		this.razonSocial = razonSocial;
	}
	public String getRubro() {
		return rubro;
	}
	public void setRubro(String rubro) {
		this.rubro = rubro;
	}
	public double getRuc() {
		return ruc;
	}
	public void setRuc(double ruc) {
		this.ruc = ruc;
	}
	public String getNombreComercial() {
		return nombreComercial;
	}
	public void setNombreComercial(String nombreComercial) {
		this.nombreComercial = nombreComercial;
	}
	public String getPais() {
		return pais;
	}
	public void setPais(String pais) {
		this.pais = pais;
	}
	public String getCiudad() {
		return ciudad;
	}
	public void setCiudad(String ciudad) {
		this.ciudad = ciudad;
	}
	public String getReferencia() {
		return referencia;
	}
	public void setReferencia(String referencia) {
		this.referencia = referencia;
	}
	public String getProvincia() {
		return provincia;
	}
	public void setProvincia(String provincia) {
		this.provincia = provincia;
	}
	public String getDireccion() {
		return direccion;
	}
	public void setDireccion(String direccion) {
		this.direccion = direccion;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getContacto() {
		return contacto;
	}
	public void setContacto(String contacto) {
		this.contacto = contacto;
	}
	public double getFax() {
		return fax;
	}
	public void setFax(double fax) {
		this.fax = fax;
	}
	public double getTelefono1() {
		return telefono1;
	}
	public void setTelefono1(double telefono1) {
		this.telefono1 = telefono1;
	}
	public double getTelefono2() {
		return telefono2;
	}
	public void setTelefono2(double telefono2) {
		this.telefono2 = telefono2;
	}
}
